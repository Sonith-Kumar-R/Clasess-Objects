package InheritanceExample;

public class Dog extends Animal{
    public void Bark(){
        System.out.println("Dog Can Bark");
    }
    public void see(){
        System.out.println("Dog can see");
    }
    public void Breathe(){
        System.out.println("Dog can Breathe");
    }
    public void legs(){
        System.out.println("Dog have legs");
    }
}
